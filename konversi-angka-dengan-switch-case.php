<?php
// Mengatur angka yang akan dikonversi
$angka = 3;

// Konversi angka ke dalam huruf
switch ($angka) {
  case 1:
    echo "Satu";
    break;
  case 2:
    echo "Dua";
    break;
  case 3:
    echo "Tiga";
    break;
  case 4:
    echo "Empat";
    break;
  case 5:
    echo "Lima";
    break;
  default:
    echo "Angka tidak dikenali";
}
?>
